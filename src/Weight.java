
public class Weight {

	private Float time;
	private Float distance;
	public Weight(Float time, Float distance) {
		super();
		this.time = time;
		this.distance = distance;
	}
	public Float getTime() {
		return time;
	}
	public void setTime(Float time) {
		this.time = time;
	}
	public Float getDistance() {
		return distance;
	}
	public void setDistance(Float distance) {
		this.distance = distance;
	}
}
